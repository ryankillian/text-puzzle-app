<h1>About This Website</h1>
<p>
	This website is a Bible study tool designed to help you engage with scripture in an interactive
	way. It presents a random (or chosen) chapter from the King James Bible with the verses shuffled.
	Your task is to rearrange the verses into their correct order.
</p>
<p>
	Once completed, you'll receive a "well done" message as feedback. This feature encourages close
	reading and deeper understanding of the text.
</p>
<p>
	The site was built with SvelteKit and uses the 'swapy' library for drag-and-drop functionality.
</p>
